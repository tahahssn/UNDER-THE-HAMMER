def search_player(name: str):
    """Fuzzy search — returns best match or None."""
    name_lower = name.lower().strip()
    if not name_lower:
        return None, None

    # Exact match first
    for player in PLAYER_DB:
        if player.lower() == name_lower:
            return player, PLAYER_DB[player]

    # Partial match
    matches = [p for p in PLAYER_DB if name_lower in p.lower()]
    if matches:
        return matches[0], PLAYER_DB[matches[0]]

    # Reverse partial (first name only etc.)
    matches = [p for p in PLAYER_DB if any(
        part in p.lower() for part in name_lower.split()
    )]
    if matches:
        return matches[0], PLAYER_DB[matches[0]]

    return None, None
